void HAL_RTC_AlarmAEventCallback(RTC_HandleTypeDef *hrtc)
{
  app_data.app_exit = MCU_WAKEUP_RTC;
  /* Clear Wake Up Flag */
  __HAL_PWR_CLEAR_FLAG(PWR_FLAG_WU);
}

void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
  if (GPIO_Pin == USB_IN_Pin)
  {
    app_data.app_exit = MCU_WAKEUP_USB;
  }
  else if (GPIO_Pin == MCU_WAKEUP_Pin)
  {
    app_data.app_exit = MCU_WAKEUP_SOC;
  }
  else if (GPIO_Pin == BUTTON_Pin)
  {
    app_data.app_exit = MCU_WAKEUP_BTN;
  }

  /* Clear Wake Up Flag */
  __HAL_PWR_CLEAR_FLAG(PWR_FLAG_WU);
}
